				<br>
					<p>Im Adressbuch sind Name, Vorname (zum Teil), Beruf, Straße(n) und Hausnummer(n) einer Person bzw. eines Betriebs vermerkt. Fettgedruckte Namen weisen darauf hin, dass gleichzeitig eine Anzeige geschaltet wurde. Manche Namen sind mit einem Stern versehen, bei einigen steht ein in eckigen Klammern hinzugefügtes [NC], beide Bedeutungen konnten nicht ermittelt werden.
					</p>

				<br>
					<p>Mit der Zusammenstellung der Adressen beabsichtigte der Herausgeber, "unseren Landsleuten usw. ein Mittel darzubieten, durch welches sie sich wieder finden und sich gegenseitig bekannt machen können" (1). Dies sollte gleichermaßen für Geschäftsleute in Paris und in Deutschland wie für Reisende gelten. Auch sollten Nachteile durch die Unkenntnis der französischen Sprache ausgeglichen werden.
					</p>

				<br>
					<p>Die Aufnahme in das Adressbuch war kostenlos, nur Anzeigen mussten bezahlt werden (2). Das Buch selbst kostete 5 Francs und wurde in Paris und in Leipzig vertrieben. Es war so erfolgreich, dass bald eine zweite Auflage erschien. Eine Jahreszahl ist in der zweiten Auflage nicht vermerkt, doch wird es vermutlich ebenfalls 1854 gewesen sein, denn bis hin zu einem Paginierungsfehler sind beide Ausgaben inhaltlich identisch. Lediglich die Anzahl der Werbeseiten hat sich erhöht. 
					</p>

				<br>
					<h4>Herausgeber</h4>
						<p>F. A. Kronauge gründete 1852 ein Spracheninstitut, das "Institut polyglotte", bei dem man Unterricht in "allen Sprachen" nehmen und Übersetzungen anfertigen lassen konnte. Das Institut war im 2. Arrondissement, 57 rue de Richelieu, danach 222 ancienne rue de Rivoli ansässig. Über dieses Institut lief die Veröffentlichung des Adressbuchs wie auch die darin enthaltenen Anzeigen. Weitere Informationen über den Herausgeber konnten nicht gefunden werden. Vermutlich handelt es sich um ein Pseudonym.
						</p>

				<br>
					<h4>Bedeutung</h4>
						<p>Die Bedeutung des Adressbuchs liegt in der großen Anzahl der Adressen. In der Volkszählung von 1851 wurden in Paris 12.245 Deutsche und 	Österreicher gezählt. Wenn beide Zahlen stimmen, so sind im Adressbuch fast 40% der deutschsprachigen Bevölkerung von Paris aus dem Jahr 1854 erfasst (3). Darüber hinaus stammt das Adressbuch aus der - was die deutsche Einwanderung betrifft - bisher wenig erforschten zweiten Hälfte des 19. Jahrhunderts und ist als umfassende nominative Quelle ein Ausgangspunkt für weitere Forschungen.
						</p>


