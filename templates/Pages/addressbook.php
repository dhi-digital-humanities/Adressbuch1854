			<h4><?= __('Das Adressbuch') ?></h4>
			
					<p>Das "Adressbuch der Deutschen in Paris von 1854" ist ein gedrucktes Buch, in dem selbständige Deutsche, die 1854 in Paris und den angrenzenden Vororten ansässig waren, verzeichnet sind. 
					</p>

				<br>
					<p>Alphabetisch geordnet findet man in diesen frühen "Gelben Seiten" 4.772 Adressen, überwiegend von Handwerkern und Kaufleuten.
					</p>

				<br>
					<p>Das Buch ist in fünf Abschnitte unterteilt: Zuerst wird eine kurze Übersicht über Sehenswürdigkeiten von Paris und Wohnungen der Gesandten gegeben. Danach sind die Adressen abgedruckt, geordnet nach Zugehörigkeit zu einem Geschäftsbereich, nach Namen und nach Straßen. Am Ende des Buchs folgt eine dreiseitige "Geschäftsliste für In- und Ausland" sowie vier Seiten "Geschäftsempfehlungen".
					</p>

				

